    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int index = tblSV.getSelectedRow();
        if (index >= 0) {
            SinhVien sv = dsSV.get(index);
            int ret = SinhVien_DAL.delete(sv);
            if (ret > 0) {
                napTblSV();
                JOptionPane.showMessageDialog(null, "Da xoa " + ret + " sinh vien");
            } else {
                JOptionPane.showMessageDialog(null, "Xoa that bai");
            }
        }
        chon = null;
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String ten = txtTen.getText();
        String lop = txtLop.getText();
        boolean phai = radNam.isSelected();
        double diem = Double.parseDouble(txtDiem.getText());
        SinhVien sv = new SinhVien(ten, lop, phai, diem);
        int ret = SinhVien_DAL.insert(sv);
        if (ret > 0) {
            napTblSV();
            JOptionPane.showMessageDialog(null, "Da them. Ma moi la: " + ret);
        } else {
            JOptionPane.showMessageDialog(null, "Them that bai");
        }
        chon = null;
    }//GEN-LAST:event_btnThemActionPerformed

    private void tblSVMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSVMouseReleased
        int index = tblSV.getSelectedRow();
        if (index >= 0) {
            chon = dsSV.get(index);
            txtMa.setText(chon.getMa() + "");
            txtTen.setText(chon.getTen());
            txtLop.setText(chon.getLop());
            if (chon.isPhai()) {
                radNam.setSelected(true);
            } else {
                radNu.setSelected(true);
            }
            txtDiem.setText(chon.getDiem() + "");
        }
    }//GEN-LAST:event_tblSVMouseReleased

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        if (chon != null) {
            String ten = txtTen.getText();
            String lop = txtLop.getText();
            boolean phai = radNam.isSelected();
            double diem = Double.parseDouble(txtDiem.getText());
            chon.setTen(ten);
            chon.setLop(lop);
            chon.setPhai(phai);
            chon.setDiem(diem);
            int ret = SinhVien_DAL.update(chon);
            if (ret > 0) {
                napTblSV();
                JOptionPane.showMessageDialog(null, "Cap nhat " + ret + " dong");
            } else {
                JOptionPane.showMessageDialog(null, "Cap nhat that bai");
            }
            chon = null;
        }
    }//GEN-LAST:event_btnSuaActionPerformed

