    public void napTblSV() {
        dsSV = SinhVien_DAL.getAll();
        model.setRowCount(0);
        for (SinhVien sv : dsSV) {
            model.addRow(new Object[]{
                sv.getMa(),
                sv.getTen(),
                sv.getLop(),
                sv.getPhai(),
                sv.getDiem()
            });
        }
        chon = null;
    }

