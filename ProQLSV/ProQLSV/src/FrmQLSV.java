    private void lstLopMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstLopMouseReleased
        int index = lstLop.getSelectedIndex();
        if (index >= 0) {
            lopChon = dsLop.get(index);
            chuyenDsSVSangModel(lopChon);
        } else {
            lopChon = null;
            chuyenDsSVSangModel(lopChon);
        }
        svChon = null;
        txtMSV.setText("");
        txtTen.setText("");
        radNam.setSelected(true);
        txtMSV.requestFocus();
    }//GEN-LAST:event_lstLopMouseReleased

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (lopChon != null) {
            int msv = Integer.parseInt(txtMSV.getText());
            String ten = txtTen.getText();
            boolean phai = radNam.isSelected();
            SinhVien sv = new SinhVien(msv, ten, phai);
            lopChon.themSV(sv);
            chuyenDsSVSangModel(lopChon);
            txtMSV.setText("");
            txtTen.setText("");
            radNam.setSelected(true);
            txtMSV.requestFocus();
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void tblSVMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSVMouseReleased
        int index = tblSV.getSelectedRow();
        if (index >= 0) {
            svChon = lopChon.laySV(index);
            txtMSV.setText(svChon.getMsv() + "");
            txtTen.setText(svChon.getTen());
            if (svChon.isPhai()) {
                radNam.setSelected(true);
            } else {
                radNu.setSelected(true);
            }
        } else {
            svChon = null;
            txtMSV.setText("");
            txtTen.setText("");
            radNam.setSelected(true);
            txtMSV.requestFocus();
        }
    }//GEN-LAST:event_tblSVMouseReleased

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        if (svChon != null) {
            int msv = Integer.parseInt(txtMSV.getText());
            String ten = txtTen.getText();
            boolean phai = radNam.isSelected();
            svChon.setMsv(msv);
            svChon.setTen(ten);
            svChon.setPhai(phai);
            chuyenDsSVSangModel(lopChon);
            txtMSV.setText("");
            txtTen.setText("");
            radNam.setSelected(true);
            txtMSV.requestFocus();
            svChon = null;
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if (svChon != null) {
            lopChon.xoaSV(svChon);
            chuyenDsSVSangModel(lopChon);
            txtMSV.setText("");
            txtTen.setText("");
            radNam.setSelected(true);
            txtMSV.requestFocus();
            svChon = null;
        }
    }//GEN-LAST:event_btnXoaActionPerformed

