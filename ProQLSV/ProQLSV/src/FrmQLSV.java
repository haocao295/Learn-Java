    private void chuyenDsLopSangModel() {
        modelLop.clear();
        for (Lop lop : dsLop) {
            modelLop.addElement(lop);
        }
    }

    private void chuyenDsSVSangModel(Lop lop) {
        modelSV.setRowCount(0);
        if (lop != null) {
            for (SinhVien sv : lop.getDsSV()) {
                modelSV.addRow(
                        new Object[]{
                            sv.getMsv(),
                            sv.getTen(),
                            sv.getPhai()
                        }
                );
            }
        }
    }

